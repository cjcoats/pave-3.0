/******************************************************************************
 * PURPOSE: lookup - Map the mapProjection parameter array into the approriate
 *                   index into the parameters structure.
 * INPUTS:  int projection  The projection.
 *          int parameter   The parameter.
 *          int alternative The alternative.
 * OUTPUTS: None
 * RETURNS: The index in the parameters table.
 * NOTES:
 *****************************************************************************/

static int lookup ( int projection, int parameter, int alternative )
    {
    PRE3 ( IN_RANGE ( projection,  0, NUMBER_OF_PROJECTIONS         - 1 ),
           IN_RANGE ( parameter,   0, NUMBER_OF_PARAMETERS          - 1 ),
           IN_RANGE ( alternative, 0, numAlternatives ( projection ) - 1 ) );

    return projectionData[ projection ].enumMapping[ alternative ][ parameter ];
    }


/*=========================== PUBLIC FUNCTIONS ==============================*/


/******************************************************************************
 * PURPOSE: numAlternatives - Get the number of alternatives for a projection.
 * INPUTS:  int projection  The projection.
 * OUTPUTS: None
 * RETURNS: int The number of alternatives for this projection.
 * NOTES:
 *****************************************************************************/

int numAlternatives ( int projection )
    {
    PRE ( IN_RANGE ( projection, 0, NUMBER_OF_PROJECTIONS - 1 ) );

    return projectionData[ projection ].numAlternatives;
    }


